# for padrão com uso do range, inicia no 0 e faz a contagem de 10 números, chegando até o 9
for i in range(10):
    print(i)
 
# for semelhante ao anterior, a diferença que inicia a partir do primeiro digito do rangem no caso o 2
for i in range(2,10):
    print(i)

# for_range, com o número inicial, até o número que vai e de quanto em quanto vai ser
for i in range(0,20,2):
    print(i)